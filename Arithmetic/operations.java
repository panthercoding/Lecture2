public class operations
{
  public static void main(String[] args)
  {
    int num1 = 5, num2 = 3; //declaring two variables on the same line

    int num3 = num1+num2; //addition
    int num4 = num1-num2; //subtraction

    int num5 = num3*num4; //multiplication
    int num6 = num1%num2; //modulus (remainder when num1 is divided by num2)
  
    int num7 = num1/num2; //integer division (decimal gets truncated/yeeted)

    System.out.println(num1);
    System.out.println(num2);
    System.out.println(num3);
    System.out.println(num4);
    System.out.println(num5);
    System.out.println(num6);
    System.out.println(num7);

  }
}